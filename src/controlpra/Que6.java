package controlpra;

public class Que6 {

	 public static void main(String[] args) {
			for (int i=1; i<6; i++) {
				for (int j=1;j<= i;j++) {
					// j값을 i값과 같게하여
					// i가 커질떄마다 j도 같이 커지게함
					// 가로가 1씩 증가할때 세로도 1씩 증가
					System.out.print("*" +"  ");
				}
			System.out.println();
			}
	        
	    }
}