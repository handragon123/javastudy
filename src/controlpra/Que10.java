package controlpra;

public class Que10 {

	 public static void main(String[] args) {
		    for(int j=1;j<7;j++) { //  J는1부터6까지찍음
		    	// i 도 1부터 6까지 찍음
		    	for(int i = 1 ; i < 7 ; i ++) {
		    		// j 와 i를 순서대로 찍음
			    	System.out.printf("(%s, %s)",j , i );
		    	} System.out.println();
			    }
		    
	        
	    }
}