package controlpra;

public class Que5 {

	 public static void main(String[] args) {
		    int num = 0;
		    for(int j=1;j<6;j++) {
		    	for(int i = 1 ; i < 6 ; i ++) {
			    	num++; // 반복문에 num값을 대입
			    	System.out.print(num + " ");
			    	// 떨어지는 5칸 가로축 
		    	} System.out.println();
			    }
		    
	        
	    }
}