package iterationex;

public class ForEx {

	public static void main(String[] args) {
		int k = 0;
		for (int i=0 ; i<5 ; i++) { //(초기화식; 조건식; 증감식)
			k++;
			System.out.print(k + "");
		}
		for(int i=0; i<5; i++) {            
            System.out.print(i+1 + " ");
        }
		for(int i=1; i<6; i++) {            
            System.out.print(i + " ");
        }
	}
}
